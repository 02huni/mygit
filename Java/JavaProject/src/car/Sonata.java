package car;

public abstract class Sonata{
	public String color;
	public String tire;
	public int displacement;
	public String handle;
	public int tax;
	
	public Sonata(String color,String tire,int displacement,String handle,int tax) {
	this.color=color;
	this.tire=tire;
	this.displacement=displacement;
	this.handle=handle;
	this.tax=tax;
	}
	
}
